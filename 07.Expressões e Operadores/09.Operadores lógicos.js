/*
    Operadores lógicos

    2 valores booleanos, quando verificados, resultará
    em verdadeiro ou falso
*/ 

let presunto = true
let queijo = false

//  AND/E                   &&
console.log(presunto && queijo)
// os dois tem que estar true para ele ser true 

// OR/OU                    ||
console.log(presunto || queijo)
// precisa apenas que um seja true para ele tbm ser 

// NOT                       !
console.log(!presunto)
//  se algo e true ele transforma para false e assim ao contrario tbm
